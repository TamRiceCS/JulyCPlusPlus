int add(int num1, int num2)
{
    return num1 + num2;
}

int subtract(int num1, int num2)
{
    return num1 - num2;
}

int multiply(int num1, int num2)
{
    return num1 * num2;
}

int divide(int num1, int num2)
{
    return num1 / num2;
}

int iterUp(int num)
{
    return num++;
}
int iterDown(int num)
{
    return num--;
}
int multiplySelf(int num)
{
    num *= num;
    return num;
}
int divideSelf(int num)
{
    num /= num;
    return num;
}